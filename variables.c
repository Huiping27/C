#include <stdio.h>

/**
 * Demonstrates the use of basic variable types in C.
 * This is a simple main function that declares and initializes
 * variables of type int, float, and char, and then prints their values.
 */
int main() {

    int age = 25;
    float height = 5.9;
    char initial = 'A';

    printf("Age: %d, Height: %.1f, Initial: %c\n", age, height, initial);
    return 0;
}

